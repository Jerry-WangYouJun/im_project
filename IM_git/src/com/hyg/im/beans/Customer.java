package com.hyg.im.beans;

public class Customer {
	private Integer id ;
	private String customerCode;
	private String customerName;
	private String linckMan ;
	private String contactNumber;
	private String address;
	private String desciotion;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCustomerCode() {
		return customerCode;
	}
	public void setCustomerCode(String customerCode) {
		this.customerCode = customerCode;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getLinckMan() {
		return linckMan;
	}
	public void setLinckMan(String linckMan) {
		this.linckMan = linckMan;
	}
	public String getContactNumber() {
		return contactNumber;
	}
	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDesciotion() {
		return desciotion;
	}
	public void setDesciotion(String desciotion) {
		this.desciotion = desciotion;
	}
	

}
